		array_push($buttons, "ishyoboy_columns_shortcodes");
		array_push($buttons, "ishyoboy_typography_shortcodes");
		array_push($buttons, "ishyoboy_block_shortcodes");
		array_push($buttons, "ishyoboy_special_shortcodes");
		return $buttons;
	}
}

if ( !function_exists('ishyoboy_add_plugin') ) {
	function ishyoboy_add_plugin($plugin_array) {
		global $tinymce_version;

		if ( version_compare( $tinymce_version, '4000' ) >= 0 ) {
			$plugin_array['ishyoboy_shortcodes_select'] = IYB_FRAMEWORK_URI . '/libs/js/shortcodes_4.js';
		} else {
			$plugin_array['ishyoboy_shortcodes_select'] = IYB_FRAMEWORK_URI . '/libs/js/shortcodes.js';
		}

		return $plugin_array;
	}
}

add_action('init', 'ishyoboy_register_tinymce_buttons');


/*
 *  Adds a custom stylesheet to the tinymce editor.
 */
if ( !function_exists('ishyoboy_register_tinymce_editor_styles') ) {
	function ishyoboy_register_tinymce_editor_styles( $url ) {

		if ( ! empty($url) )
			$url .= ',';

		// Change the path here if using sub-directory
		global $tinymce_version;

		if ( version_compare( $tinymce_version, '4000' ) >= 0 ) {
			$url .= IYB_FRAMEWORK_URI . '/libs/css/tinymce_4.css';
		} else {
			$url .= IYB_FRAMEWORK_URI . '/libs/css/tinymce.css';
		}

		return $url;

	}
}

if ( !function_exists('ishyoboy_register_admin_tinymce_styles') ) {
	function ishyoboy_register_admin_tinymce_styles() {
		/* Register our stylesheet. */
		global $tinymce_version;

		if ( version_compare( $tinymce_version, '4000' ) >= 0 ) {
			wp_register_style( 'myPluginStylesheet', IYB_FRAMEWORK_URI . '/libs/css/tinymce_4.css' );
		} else {
			wp_register_style( 'myPluginStylesheet', IYB_FRAMEWORK_URI . '/libs/css/tinymce.css' );
		}

		wp_enqueue_style( 'myPluginStylesheet' );
	}
}

add_action( 'admin_init', 'ishyoboy_register_admin_tinymce_styles' );
add_filter( 'mce_css', 'ishyoboy_register_tinymce_editor_styles' );