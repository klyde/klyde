                }
                else{
                    $return .= '</div><div class="space"></div><div class="row">';
                }
            }

        } // Endwhile

        if ( 'yes' == $sc_atts['slideshow'] ){
            $return .= '[/slide][/slidable]';
        }
        else{
            $return .= '</div>';
        }
    }

    wp_reset_query();

    return do_shortcode($return);
}
